const color = 'green';

switch (color){

case 'white':
  console.log('The color is white');
  break;

case 'black':
  console.log('The color is black');
  break;
case 'green':
  console.log('The color is green');
  break;
case 'red':
  console.log('The color is red');
  break;
default:
  console.log('Unknown color');
}